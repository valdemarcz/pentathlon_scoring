package PentathlonScoring;

public class Athlete{
	
	private String name;
	private int fencingVictories;
	private String swimmingTime;
	private int ridingKnockingDown;
	private int ridingRefusal;
	private int ridingDiobedienceLeading;
	private int shootingTargetScore;
	private String runTime;
	private int finalPoints;
	private int concludingEvent;
	private int place;
	private String concludingEventTime;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	public int getFencingVictories() {
		return fencingVictories;
	}

	public void setFencingVictories(int fencingVictories) {
		this.fencingVictories = fencingVictories;
	}

	public String getSwimmingTime() {
		return swimmingTime;
	}

	public void setSwimmingTime(String swimmingTime) {
		this.swimmingTime = swimmingTime;
	}

	public int getRidingKnockingDown() {
		return ridingKnockingDown;
	}

	public void setRidingKnockingDown(int ridingKnockingDown) {
		this.ridingKnockingDown = ridingKnockingDown;
	}

	public int getRidingRefusal() {
		return ridingRefusal;
	}

	public void setRidingRefusal(int ridingRefusal) {
		this.ridingRefusal = ridingRefusal;
	}

	public int getRidingDiobedienceLeading() {
		return ridingDiobedienceLeading;
	}

	public void setRidingDiobedienceLeading(int ridingDiobedienceLeading) {
		this.ridingDiobedienceLeading = ridingDiobedienceLeading;
	}

	public int getShootingTargetScore() {
		return shootingTargetScore;
	}

	public void setShootingTargetScore(int shootingTargetScore) {
		this.shootingTargetScore = shootingTargetScore;
	}

	public String getRunTime() {
		return runTime;
	}

	public void setRunTime(String runTime) {
		this.runTime = runTime;
	}
	
	public int getFinalPoints() {
		return finalPoints;
	}

	public void setFinalPoints(int finalPoints) {
		this.finalPoints = finalPoints;
	}
	
	
	public int getConcludingEvent() {
		return concludingEvent;
	}

	public void setConcludingEvent(int concludingEvent) {
		this.concludingEvent = concludingEvent;
	}

	public int getPlace() {
		return place;
	}

	public void setPlace(int place) {
		this.place = place;
	}

	public String getConcludingEventTime() {
		return concludingEventTime;
	}

	public void setConcludingEventTime(String concludingEventTime) {
		this.concludingEventTime = concludingEventTime;
	}
	
	
	
}
